    public String generateMaPN() {
        PhieuNhap pn = PhieuNhapDAO.getInstance().getLastPhieuNhap();
        int sothutu = (Integer.parseInt(pn.getMaPN().substring(2)) + 1);
        String maPN = "PN";
        if (sothutu < 10) {
            maPN = "PN00";
        } else if (sothutu < 100) {
            maPN = "PN0";
        }
        maPN += sothutu;
        return maPN;
    }
