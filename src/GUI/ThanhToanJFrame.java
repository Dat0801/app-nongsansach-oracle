    public String generateMaHD() {
        HoaDon hoadon = HoaDonDAO.getInstance().getLastHoaDon();
        if(hoadon == null) {
            
        }
        int sothutu = (Integer.parseInt(hoadon.getMaHD().substring(2)) + 1);
        String mahd = "HD";
        if (sothutu < 10) {
            mahd = "HD00";
        } else if (sothutu < 100) {
            mahd = "HD0";
        }
        mahd += sothutu;
        return mahd;
    }

